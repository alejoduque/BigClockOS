char *makeBinaryString(uint64_t value,bool spaceIt)
{
   int i;
   char *ptr = spaceIt ? binarySpaceString : binaryString;
   char *org = ptr;
   for(i=0;i<64;i++)
   {
      if(value &  (1ul<<63))
      {
         *ptr++ = '1';
      }
      else
      {
         *ptr++ = '0';
      }
      value <<= 1;

      if(spaceIt && (((i+1) & 3) == 0))
      {
         *ptr++ = ' ';
      }
   }
   *ptr++ = 0;
   return(org);
}

